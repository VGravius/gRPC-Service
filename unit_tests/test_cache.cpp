#include <gtest/gtest.h>
#include "../src/cache.h"

// =============================================================================
//  Тест: SetAndGet
// =============================================================================

/**
 * @brief Проверяет сохранение и получение значения по ключу.
 *
 * Сценарий:
 * 1. Создаётся пустой кэш.
 * 2. Сохраняется пара "key" → "value".
 * 3. Выполняется запрос по ключу "key".
 * 4. Проверяется, что значение существует и равно "value".
 *
 * @test Проверяет, что Cache::Set и Cache::Get работают корректно.
 */
TEST(CacheTest, SetAndGet) {
    Cache cache;
    cache.Set("key", "value");
    auto result = cache.Get("key");

    ASSERT_TRUE(result.has_value());          // Значение должно существовать
    EXPECT_EQ(result.value(), "value");       // Значение должно быть "value"
}

// =============================================================================
//  Тест: GetNotFound
// =============================================================================

/**
 * @brief Проверяет поведение при запросе несуществующего ключа.
 *
 * Сценарий:
 * 1. Создаётся пустой кэш.
 * 2. Выполняется запрос по ключу "unknown", которого нет в кэше.
 * 3. Проверяется, что возвращается std::nullopt.
 *
 * @test Проверяет, что Cache::Get возвращает std::nullopt для отсутствующего ключа.
 */
TEST(CacheTest, GetNotFound) {
    Cache cache;
    auto result = cache.Get("unknown");

    EXPECT_FALSE(result.has_value());         // Значение должно отсутствовать
}

// =============================================================================
//  Тест: Exists
// =============================================================================

/**
 * @brief Проверяет метод Cache::Exists.
 *
 * Сценарий:
 * 1. Создаётся пустой кэш.
 * 2. Сохраняется ключ "key".
 * 3. Проверяется, что Exists("key") возвращает true.
 * 4. Проверяется, что Exists("unknown") возвращает false.
 *
 * @test Проверяет корректность проверки существования ключа.
 */
TEST(CacheTest, Exists) {
    Cache cache;
    cache.Set("key", "value");

    EXPECT_TRUE(cache.Exists("key"));         // Существующий ключ → true
    EXPECT_FALSE(cache.Exists("unknown"));    // Несуществующий ключ → false
}

// =============================================================================
//  Тест: Overwrite
// =============================================================================

/**
 * @brief Проверяет перезапись значения при сохранении по существующему ключу.
 *
 * Сценарий:
 * 1. Создаётся пустой кэш.
 * 2. Сохраняется пара "key" → "value1".
 * 3. Сохраняется пара "key" → "value2" (перезапись).
 * 4. Выполняется запрос по ключу "key".
 * 5. Проверяется, что значение равно "value2" (новое значение).
 *
 * @test Проверяет, что Cache::Set перезаписывает существующее значение.
 */
TEST(CacheTest, Overwrite) {
    Cache cache;
    cache.Set("key", "value1");
    cache.Set("key", "value2");

    auto result = cache.Get("key");
    EXPECT_EQ(result.value(), "value2");      // Должно быть новое значение
}