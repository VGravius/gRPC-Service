#include <gtest/gtest.h>
#include "../src/logger.h"

// =============================================================================
//  Тест: Log
// =============================================================================

/**
 * @brief Проверяет метод Logger::Log.
 *
 * Сценарий:
 * 1. Вызывается Logger::Log с полным набором параметров.
 * 2. Проверяется, что вызов не вызывает исключений.
 *
 * @test Проверяет, что Logger::Log работает без ошибок.
 */
TEST(LoggerTest, Log) {
    Logger logger;

    // Вызов логирования с деталями
    EXPECT_NO_THROW(logger.Log("GetParameter", "temperature", "OK", "value: 23.5"));
}

// =============================================================================
//  Тест: LogError
// =============================================================================

/**
 * @brief Проверяет метод Logger::LogError.
 *
 * Сценарий:
 * 1. Вызывается Logger::LogError с сообщением об ошибке.
 * 2. Проверяется, что вызов не вызывает исключений.
 *
 * @test Проверяет, что Logger::LogError работает без ошибок.
 */
TEST(LoggerTest, LogError) {
    Logger logger;

    // Вызов логирования ошибки
    EXPECT_NO_THROW(logger.LogError("Test error message"));
}

// =============================================================================
//  Тест: LogWithoutDetails
// =============================================================================

/**
 * @brief Проверяет метод Logger::Log без дополнительных деталей.
 *
 * Сценарий:
 * 1. Вызывается Logger::Log без параметра details.
 * 2. Проверяется, что вызов не вызывает исключений.
 *
 * @test Проверяет, что Logger::Log работает с параметром по умолчанию.
 */
TEST(LoggerTest, LogWithoutDetails) {
    Logger logger;

    // Вызов логирования без деталей
    EXPECT_NO_THROW(logger.Log("SetParameter", "temperature", "OK"));
}