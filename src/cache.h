#pragma once

#include <unordered_map>  // Для хранения данных
#include <optional>       // Для возврата "ничего" (nullopt)
#include <string>         // Для ключей и значений
#include <mutex>          // Для потокобезопасности

/**
 * @brief Потокобезопасное хранилище параметров в памяти.
 *
 * Класс Cache предоставляет интерфейс для сохранения, получения и проверки
 * наличия параметров в памяти. Все методы защищены мьютексом, что позволяет
 * безопасно использовать объект из нескольких потоков одновременно.
 *
 * @note Использует std::unordered_map для хранения данных (O(1) доступ).
 * @note std::mutex защищает данные от состояния гонки.
 */
class Cache {
public:
    /**
     * @brief Сохраняет пару "ключ-значение" в кэш.
     *
     * Если ключ уже существует, его значение будет перезаписано.
     * Метод потокобезопасен.
     *
     * @param key   Имя параметра (ключ). Не должно быть пустым.
     * @param value Значение параметра.
     */
    void Set(const std::string& key, const std::string& value);

    /**
     * @brief Возвращает значение параметра по ключу.
     *
     * Если ключ найден — возвращает его значение, обёрнутое в std::optional.
     * Если ключ не найден — возвращает std::nullopt.
     * Метод потокобезопасен.
     *
     * @param key Имя параметра (ключ).
     * @return std::optional<std::string> Значение параметра или nullopt.
     */
    std::optional<std::string> Get(const std::string& key) const;

    /**
     * @brief Проверяет наличие ключа в кэше.
     *
     * Метод потокобезопасен.
     *
     * @param key Имя параметра (ключ).
     * @return true  — ключ существует.
     * @return false — ключ отсутствует.
     */
    bool Exists(const std::string& key) const;

private:
    std::unordered_map<std::string, std::string> data_;
    mutable std::mutex mutex_;  ///< Мьютекс для защиты данных. mutable — для использования в const-методах.
};