#pragma once

#include <string>  // Для работы со строками

/**
 * @brief Класс для логирования операций в консоль.
 *
 * Предоставляет два основных метода:
 * - Log()     — для записи информационных сообщений об операциях.
 * - LogError() — для записи сообщений об ошибках.
 *
 * Все сообщения автоматически дополняются временной меткой
 * в формате "YYYY-MM-DD HH:MM:SS.mmm".
 *
 * @note Логи выводятся в стандартный поток вывода (std::cout).
 * @note Время формируется в локальном часовом поясе.
 */
class Logger {
public:
    /**
     * @brief Записывает информацию об операции в консоль.
     *
     * Формат вывода:
     * [YYYY-MM-DD HH:MM:SS.mmm] operation: name - status (details)
     *
     * Поле details является опциональным. Если оно не указано,
     * скобки с деталями не выводятся.
     *
     * @param operation Название операции (например, "GetParameter").
     * @param name      Имя параметра или другая сущность.
     * @param status    Статус операции (например, "OK", "NOT_FOUND").
     * @param details   Дополнительная информация (опционально, по умолчанию "").
     *
     * @example
     * Log("GetParameter", "temperature", "OK", "value: 23.5");
     * // [2026-06-20 15:30:00.123] GetParameter: temperature - OK (value: 23.5)
     */
    void Log(const std::string& operation,
        const std::string& name,
        const std::string& status,
        const std::string& details = "");

    /**
     * @brief Записывает сообщение об ошибке в консоль.
     *
     * Формат вывода:
     * [YYYY-MM-DD HH:MM:SS.mmm] ОШИБКА: message
     *
     * Используется для логирования критических ситуаций:
     * - Невалидные входные данные.
     * - Внутренние ошибки сервера.
     * - Исключительные ситуации.
     *
     * @param message Текст сообщения об ошибке.
     *
     * @example
     * LogError("SetParameter: invalid name (empty)");
     * // [2026-06-20 15:30:05.456] ОШИБКА: SetParameter: invalid name (empty)
     */
    void LogError(const std::string& message);

private:
    /**
     * @brief Возвращает текущее время в виде форматированной строки.
     *
     * Формат: "YYYY-MM-DD HH:MM:SS.mmm"
     * - Год-месяц-день час:минута:секунда.миллисекунды
     *
     * @return std::string Текущее время в локальном часовом поясе.
     */
    std::string GetCurrentTime() const;
};