#include "cache.h"

// =============================================================================
//  Реализация метода Set
// =============================================================================

/**
 * @brief Сохраняет пару "ключ-значение" в кэш.
 *
 * Метод блокирует мьютекс, затем выполняет вставку или обновление
 * значения по указанному ключу. Если ключ уже существует, значение
 * будет перезаписано.
 *
 * @param key   Имя параметра (ключ).
 * @param value Значение параметра.
 *
 * @note Использует std::lock_guard для автоматической блокировки
 *       и освобождения мьютекса (RAII).
 */
void Cache::Set(const std::string& key, const std::string& value) {
    std::lock_guard<std::mutex> lock(mutex_);  // Блокировка мьютекса
    data_[key] = value;                        // Сохранение или обновление
    // Мьютекс автоматически освобождается при выходе из функции
}

// =============================================================================
//  Реализация метода Get
// =============================================================================

/**
 * @brief Возвращает значение параметра по ключу.
 *
 * Метод блокирует мьютекс, затем выполняет поиск ключа в хеш-таблице.
 * Если ключ найден — возвращает его значение, обёрнутое в std::optional.
 * Если ключ не найден — возвращает std::nullopt.
 *
 * @param key Имя параметра (ключ).
 * @return std::optional<std::string> Значение параметра или nullopt.
 *
 * @note Метод объявлен как const, но мьютекс помечен как mutable,
 *       поэтому блокировка возможна даже в const-методах.
 */
std::optional<std::string> Cache::Get(const std::string& key) const {
    std::lock_guard<std::mutex> lock(mutex_);  // Блокировка мьютекса
    auto it = data_.find(key);                 // Поиск ключа
    if (it != data_.end()) {
        return it->second;                     // Ключ найден — возвращаем значение
    }
    return std::nullopt;                       // Ключ не найден
}

// =============================================================================
//  Реализация метода Exists
// =============================================================================

/**
 * @brief Проверяет наличие ключа в кэше.
 *
 * Метод блокирует мьютекс, затем проверяет, существует ли указанный ключ
 * в хеш-таблице.
 *
 * @param key Имя параметра (ключ).
 * @return true  — ключ существует.
 * @return false — ключ отсутствует.
 *
 * @note Метод объявлен как const, но мьютекс помечен как mutable.
 */
bool Cache::Exists(const std::string& key) const {
    std::lock_guard<std::mutex> lock(mutex_);  // Блокировка мьютекса
    return data_.find(key) != data_.end();     // Поиск и возврат bool
}